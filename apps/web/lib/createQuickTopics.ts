const VISIBLE_TOPIC_COUNT = 4;

function shuffleTopics(seed: number, pool: CreateQuickTopic[]): CreateQuickTopic[] {
  const arr = [...pool];
  let s = Math.max(1, Math.floor(seed)) % 2147483647;
  const rnd = () => {
    s = (s * 48271) % 2147483647;
    return s / 2147483647;
  };
  for (let i = arr.length - 1; i > 0; i--) {
    const j = Math.floor(rnd() * (i + 1));
    [arr[i], arr[j]] = [arr[j], arr[i]];
  }
  return arr;
}

/**
 * @param seed 变化时得到不同一批展示项（与「刷新」按钮联动）
 */
export function pickQuickTopicsForDisplay(seed: number, pool = CREATE_QUICK_TOPIC_POOL): CreateQuickTopic[] {
  const shuffled = shuffleTopics(seed, pool);
  return shuffled.slice(0, Math.min(VISIBLE_TOPIC_COUNT, shuffled.length));
}
